import UIKit

extension UIColor {
    class func gn_blackColor(alpha: CGFloat = 1.0) -> UIColor {
        return UIColor(red: 0.020, green: 0.040, blue: 0.130, alpha: alpha)
    }
    
    class func gn_yellowColor(alpha: CGFloat = 1.0) -> UIColor {
        return UIColor(red: 1.000, green: 0.940, blue: 0.000, alpha: alpha)
    }

}
